# Arithmetic Operators
x= 34
y= 17

print("ADDITION", x + y)
print("SUBTRACTION", x - y)
print("MULTIPLY", x * y)
print("DIVIDE", x / y)
print("FLOOR DIVIDE", x // y)
print("REMAINDER", x % y)
print("EXPONENT", x ** y)

print("--------------------")

# Relational or Comparison Operator
print("Equals", x == y)
print("Not Equals", x != y)
print("x less than or equal to y", x <= y)
print("x greater than or equal to y", x >= y)
print("x less than y", x < y)
print("x greater than y", x > y)

print("--------------------")

# Logical Operators
print("Use of and", (x != y) and (x >= y)) 
print("Use of or", (x != y) or (x >= y) ) 
print("Use of not", not (x != y)) 

print( not 2, not 0, not -4)
print( x and y) # returns x if x is false, otherwise y
print( x or y) # returns y if x is false, otherwise x
#positive int -> true, 0 -> false, negative -> true

print(not "", not "abs")

